    fun part1(lines: Sequence<String>): Int {
        return sequence {
            var x = 1
            var clock = 0

            suspend fun SequenceScope<Int>.tick() {
                clock += 1
                val interestOffset = 20
                val interestRepeat = 40

                val col = (clock % 40) - 1
                val range = (col - 1).coerceAtLeast(0)..(col + 1).coerceAtMost(40)
                print(if (x in range) "#" else ".")
                if (clock == interestOffset || (clock - interestOffset) % interestRepeat == 0) {
                    val signal = x * clock
                    yield(signal)
                }
                if (col + 1 == 0) {
                    println()
                }
            }

            for (line in lines) {
                if (line == "noop") {
                    tick()
                } else {
                    tick()
                    tick()
                    x += line.split(' ')[1].toInt()
                }
            }
            println()
        }.sum()
    }

